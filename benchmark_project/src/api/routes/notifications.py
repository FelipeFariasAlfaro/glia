"""
Notification API Routes.

Manages notification preferences, templates, and delivery status.
Supports email, push notifications, and webhook callbacks.
Notifications are triggered by domain events (not direct API calls from other services).
"""

from fastapi import APIRouter, Depends, HTTPException, Query
from typing import List, Optional
from uuid import UUID

from src.services.auth_service import get_current_user, require_role
from src.services.notification_service import NotificationService

router = APIRouter(prefix="/notifications", tags=["notifications"])


@router.get("/")
async def list_notifications(
    current_user=Depends(get_current_user),
    unread_only: bool = False,
    page: int = Query(1, ge=1),
    page_size: int = Query(20, ge=1, le=100)
):
    """List notifications for the current user.
    
    Notifications are generated by the event system when:
    - Order status changes (order_service emits events)
    - Payment is processed (payment_service emits events)
    - Low stock alerts (inventory_service emits events)
    
    The notification_service subscribes to these events via Redis pub/sub.
    It does NOT import or call other services directly (event-driven decoupling).
    """
    svc = NotificationService()
    return await svc.get_user_notifications(
        user_id=current_user.id, unread_only=unread_only,
        page=page, page_size=page_size
    )


@router.post("/{notification_id}/read", status_code=204)
async def mark_as_read(notification_id: UUID, current_user=Depends(get_current_user)):
    """Mark a notification as read."""
    svc = NotificationService()
    await svc.mark_read(notification_id, current_user.id)


@router.get("/preferences")
async def get_preferences(current_user=Depends(get_current_user)):
    """Get user notification preferences (channels, frequency, muted categories)."""
    svc = NotificationService()
    return await svc.get_preferences(current_user.id)


@router.put("/preferences")
async def update_preferences(prefs: dict, current_user=Depends(get_current_user)):
    """Update notification preferences.
    
    Users can opt out of specific channels (email, push) or mute categories.
    Webhook URLs are validated before saving (must be HTTPS, respond to ping).
    """
    svc = NotificationService()
    return await svc.update_preferences(current_user.id, prefs)


@router.post("/webhooks/register")
async def register_webhook(
    url: str, events: List[str],
    current_user=Depends(get_current_user)
):
    """Register a webhook endpoint for specific event types.
    
    Webhooks are delivered with exponential backoff (max 5 retries).
    Delivery is handled by webhook_worker (background Celery task).
    Failed deliveries after all retries trigger an alert to the user.
    """
    svc = NotificationService()
    webhook = await svc.register_webhook(
        user_id=current_user.id, url=url, events=events
    )
    return {"webhook_id": webhook.id, "status": "active"}


@router.delete("/webhooks/{webhook_id}", status_code=204)
async def delete_webhook(webhook_id: UUID, current_user=Depends(get_current_user)):
    """Deactivate a webhook endpoint."""
    svc = NotificationService()
    await svc.deactivate_webhook(webhook_id, current_user.id)
